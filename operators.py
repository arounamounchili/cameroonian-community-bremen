"""
Arithmetic operators in Python
"""

# Addition: use the + sign
print(10 + 33 + 2)  # 45

# Subtraction
print(10 - 3)  # 7

# Multiplication
print(12 * 4)  # 48

# Division
print(13 / 2)  # 6.5
print(10 / 2)  # 5.0
print(10 // 2)  # 5 (use a double forward slashes to get integer)

# Find a remainder divided by another number
print(19 % 3)  # 1 (3*6 + 1)
print(263 % 2)

# Power
print(4 ** 2)  # 16
print(2 ** 3)  # 8
print(pow(10, 2))
print()

# max and min
x = min([3, 66, 2, 56])
y = max(16, 64, 7, 88)
print("min: ", x)
print("max: ", y)
